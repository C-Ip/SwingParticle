public class BallParticles {
    
    int radius;
    int x;
    int y;
    int velX;
    int velY;
    
    BallParticles(int rad, int x, int y, int velx, int vely) {
        this.radius = rad;
        this.x = x;
        this.y = y;
        this.velX = velx;
        this.velY = vely;
    }
}
